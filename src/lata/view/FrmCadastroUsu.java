    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        AcessoDAO dao = null;

        if (txtSenha.getText().equals(txtConfirmarSenha.getText())) {
            try {
                dao = new AcessoDAO();
                Acesso u = new Acesso();
                boolean statusEmail = u.validacaoDoEmail(txtEmail.getText());
                boolean statusSenha = u.validacaoSenha(txtSenha.getText());
                if (statusEmail == true) {
                    if (statusSenha == true) {
                        u.setNome(txtNome.getText());
                        u.setSexo(escolherSexo());
                        u.setDataAni(u.formatarData(txtDataAni.getText()));
                        u.setCelular(u.formatarNumero(txtCelular.getText()));
                        u.setEmail(txtEmail.getText());
                        u.setSenha(txtSenha.getText());
                        dao.create(u);
                    }else{
                        JOptionPane.showMessageDialog(null, "A senha n√£o se encaixa nos requisitos do programa, para se encaixar digite uma senha que: \n \n Tenha pelo menos uma letra. \n tenha pelo menos um n√∫mero. \n tenha pelo menos 8 caracteres.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Email inv√°lido!");
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "As senhas informadas n√£o coincidem");
        }

    }//GEN-LAST:event_btnSalvarActionPerformed

